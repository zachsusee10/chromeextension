document.addEventListener('DOMContentLoaded', function() {
    const teamsInput = document.getElementById('teams');
    const suggestions = document.getElementById('suggestions');
    const searchBtn = document.getElementById('searchBtn');
    const resultDiv = document.getElementById('result');

    // Focus the input field when the popup opens
    teamsInput.focus();
    
    // Event listener for input field (autocomplete functionality)
    teamsInput.addEventListener('input', function() {
        const query = teamsInput.value.toLowerCase();
        suggestions.innerHTML = '';

        if (query) {
            const filteredTeams = Object.keys(teamBroadcastLinks).filter(team => 
                team.toLowerCase().includes(query)
            );

            filteredTeams.forEach(team => {
                const suggestionItem = document.createElement('li');
                suggestionItem.textContent = team;
                suggestionItem.addEventListener('click', function() {
                    teamsInput.value = team;
                    suggestions.innerHTML = '';
                });
                suggestions.appendChild(suggestionItem);
            });
        }
    });

    // Handle the search button click
    searchBtn.addEventListener('click', function() {
        const selectedTeam = teamsInput.value.trim();
        if (selectedTeam && teamBroadcastLinks[selectedTeam]) {
            window.open(teamBroadcastLinks[selectedTeam], '_blank');
        } else {
            resultDiv.textContent = 'No broadcast link available for this team.';
        }
    });
});

  


